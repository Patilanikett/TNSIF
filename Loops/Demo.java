

public class Demo {
    public static void main(String[] args) {
        // do while loop example
        System.out.println("\ndo while loop example");
        int no_of_iteration = 10;
        int count = 1;
        do {
            System.out.println("Iteration " + count);
            count++;
        } while (count <= no_of_iteration);
    }
    
}
